/*
* Vectores: Primera forma de crear vectores
* let vector_nombre = vec! [val1, val2, val3……];
*/

fn main()  {     
  let v =vec![100, 200, 300, 400];          // creamos un vector
  println!("Primer elemento  :{}",v[0]);    // accedemos por posición al primer elemento
  println!("Segundo elemento :{}",v[1]);    
  println!("Tercer elemento  :{}",v[2]); 
  println!("Cuarto elemento  :{}",v[3]); 
}