fn main() {
  let x:i32 = 100;
  let y:i32 = 200;
  println!("x es mayor que y : {}", x > y);
  println!("x es menor que y : {}", x < y);
  println!("x es NOT igual a y : {}", x != y);
  println!("x es mayor o igual a y : {}", x >= y);
  println!("x es menor o igual a y : {}", x <= y);
  println!("x es igual a y : {}", x == y);
}